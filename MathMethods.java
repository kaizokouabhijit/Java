package assignment;

public class MathMethods {
	
	public static void main(String [] args) {
		System.out.println(Math.max(15,15.00000000000001)); // returns the max of the two arguments
		
		System.out.println(Math.min(15, 15.0000000000001)); // returns the min of the two arguments
		
		System.out.println(Math.sqrt(64)); // returns the square root of the argument passed
		
		System.out.println(Math.abs(-4.5)); // Returns the absolute value of parameter
	
		System.out.println(Math.random()); // generates a number between 0 and 1.0(not inclusive)
		
		System.out.println(Math.random()); // different random number this time
		
		System.out.println(Math.round(4.5)); // rounds the number to the nearest integer
	}

}
